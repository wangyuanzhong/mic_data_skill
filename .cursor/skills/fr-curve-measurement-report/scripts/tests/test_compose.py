def test_compose_cli_reads_note_files(synthetic_ready_dir: Path, monkeypatch):
    import compose_html as mod

    fig_dir = synthetic_ready_dir / "figures"
    fig_dir.mkdir(exist_ok=True)
    (fig_dir / "奇异值与剔异前均值.png").write_bytes(b"x")
    sec = synthetic_ready_dir / "report_sections"
    sec.mkdir()
    (sec / "intro_note.html").write_text("<p>文件介绍</p>", encoding="utf-8")
    (sec / "sensitivity_note.html").write_text("<p>文件灵敏度</p>", encoding="utf-8")
