@pytest.mark.integration
def test_pdf_loads_relative_chinese_named_png(tmp_path: Path):
    """Regression: Chromium blocks file:// images under set_content+<base>; must use goto."""
    from playwright.sync_api import sync_playwright

    from compose_pdf import load_report_html

    fig_dir = tmp_path / "figures"
    fig_dir.mkdir()
    (fig_dir / "金标绝对与偏差.png").write_bytes(_MINI_PNG)
    html = tmp_path / "report.html"
    html.write_text(
        "<!DOCTYPE html><html><head><meta charset='utf-8'/></head>"
        "<body><img src='figures/金标绝对与偏差.png' alt='fig'/></body></html>",
        encoding="utf-8",
    )
    try:
        with sync_playwright() as p:
            browser = p.chromium.launch()
            page = browser.new_page()
            load_report_html(page, html)
            info = page.evaluate(
                """() => Array.from(document.images).map(img => img.naturalWidth)"""
            )
            browser.close()
    except Exception as e:
        pytest.skip(f"playwright unavailable: {e}")
    assert info == [2], f"expected loaded 2px-wide image, got {info}"
