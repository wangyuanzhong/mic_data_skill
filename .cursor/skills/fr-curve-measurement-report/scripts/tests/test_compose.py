    (sec / "conclusion_note.html").write_text("<p>文件结论</p>", encoding="utf-8")

    monkeypatch.setattr(
        "sys.argv",
        [
            "compose_html.py",
            "--output-dir",
            str(synthetic_ready_dir),
            "--intro-note-file",
            str(sec / "intro_note.html"),
            "--sensitivity-note-file",
            str(sec / "sensitivity_note.html"),
            "--curves-notes-file",
            str(sec / "curves_notes.html"),
            "--conclusion-note-file",
            str(sec / "conclusion_note.html"),
        ],
    )
    mod.main()
    text = (synthetic_ready_dir / "report.html").read_text(encoding="utf-8")
    assert "文件介绍" in text
    assert "文件灵敏度" in text
    assert "文件曲线" in text
    assert "文件结论" in text
    assert "灵敏度明细" in text


